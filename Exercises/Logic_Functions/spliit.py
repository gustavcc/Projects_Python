'''split() itera sobre uma string, 
dividindo ela em partes de acordo com o que 
você deseja... Você pode usá-lo para dividir 
valores de string de formulários da web. Ou 
você pode até usá-lo para contar o número de
palavras em um pedaço de texto.'''

words = "column1 column2"
words = words.split(" ")
print(words) # devolve em formato de lista